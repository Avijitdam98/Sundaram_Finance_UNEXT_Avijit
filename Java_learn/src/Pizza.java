
public class Pizza {
	String braed;
	String sauce;
	String cheese;
	String topping;
	
	Pizza(String braed){
		
		this.braed=braed;
		
	}
	
Pizza(String braed,String sauce){
		
		this.braed=braed;
		this.sauce=sauce;
		
	}

	
Pizza(String braed,String sauce,String cheese,String topping){
		
		this.braed=braed;
		this.sauce=sauce;
		this.cheese=cheese;
		this.topping=topping;
	}
	
}
